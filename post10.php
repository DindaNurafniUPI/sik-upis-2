  Pada tahun 2020, terjadi perubahan besar dalam dunia pendidikan, yaitu kegiatan belajar mengajar harus dilakukan secara daring. Pandemi Covid-19 yang terjadi saat ini menuntut semua pihak untuk mau beradaptasi dengan kebiasaan-kebiasaan baru. Perkuliahan daring bukan sesuatu yang mudah bagi dosen maupun mahasiswa. Dosen dan mahasiswa harus terus belajar dan beradaptasi dengan kondisi seperti saat ini. Pada Tahun 2021 pandemi masih terus terjadi. Di Program Studi Sistem Informasi Kelautan kuliah daring dilaksanakan dengan memanfaatkan beberapa Learning management system (LMS) seperti Google Meet, Google Classroom, Zoom Cloud Meeting, maupun LMS yang dimiliki UPI yaitu SPOT.

Meskipun dilaksanakan secara daring, SIK tetap berkomitmen untuk memberikan pelayanan maksimal kepada mahasiswa dalam menambah pengetahuan dan wawasan. Salah satu upaya yang dilakukan adalah menghadirkan dosen tamu. Dosen yang diundang untuk mengisi perkuliahan tamu tersebut adalah Rahmadimi Trulyan Perkasa Rohanda, S.I.K, M.T. Ia merupakan Lulusan Magister Teknik Kelautan, Institut Teknologi Bandung, dan sebagai Peneliti kelautan bidang Coastal engineer. Perkuliahan dilakukan pada setiap hari Senin jam 7.00-08.40 WIB yaitu di tanggal 11,18, dan 25 Oktober 2021.

Kuliah tamu yang dilaksanan menggunakan Google Meet ini membahas tiga topik utama yaitu Karakteristik Massa Air laut, Biologi Oseanografi, dan Pasang Surut. Ia menyampaikan bahwa pada dasarnya, Oseanografi sendiri terdiri dari oseanografi Fisik, Kimia, Geologi dan Biologi. Oseanografi biologi adalah salah satu cabang utama ilmu oseanografi. Bahasan tentang oseanografi biologi mencakup:

Properti yang mempengaruhi kehidupan di laut, dan perbandingan dengan kehidupan di darat;
Gambaran fisika dan kimia laut yang meliputi cahaya, suhu, salinitas, densitas, dan tekanan yang sangat mempengaruhi kondisi dimana organisme laut hidup;
Memperkenalkan berbagai fitoplankton, dan menjelaskan bagaimana tumbuhan ini memproduksi senyawa organik yang kaya akan energi lewat proses fotosintesis;
Membahas tipe utama zooplankton dan pola sederhana kehidupannya serta mekanisme makan;
Memperkenalkan aliran energi lewat rantai dan jejaring makanan di laut;
Memperkenalkan berbagai tipe nekton termasuk Krustasea nektonik, reptil laut, mamalia laut, ikan laut, dan burung laut; dan
Memperkenalkan tipe utama tumbuhan dan hewan yang hidup di dasar laut.
Dengan hadirnya dosen tamu ini, menambah pengetahuan mahasiswa terkait Oseanografi dan mendapatkan gambaran mengenai penelitiian kelautan bidang Coastal engineer. [Humas SIK]

